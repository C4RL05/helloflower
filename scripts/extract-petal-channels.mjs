/**
 * Decodes the tiny (32x16) petal mask PSDs from the read-only Unity project and
 * emits their per-pixel "alpha" as a generated TS module
 * (src/engine/paint/petalChannels.generated.ts).
 *
 * Faithfulness notes:
 * - Unity's `Flower.GetAlphas` reads `pixel.a`. The color/lightmap/noise PSDs
 *   are 3-channel RGB; Unity's importer derived their alpha from grayscale
 *   ("Alpha from Grayscale"). We reproduce that: 3ch → grayscale (Unity
 *   Color.grayscale weights), 4ch → the real alpha channel.
 * - We emit values in Unity's GetPixels order (BOTTOM-up rows), matching how the
 *   runtime PetalPainter writes a bottom-origin DataTexture, so petal UV v=0
 *   (the petal base) lines up with the original.
 *
 * Run: node scripts/extract-petal-channels.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";

const DIR = "HelloFlower-Unity/Assets/3D/Flower/";
const OUT = "src/engine/paint/petalChannels.generated.ts";

// channelKey → source PSD. shadow uses PetalTexture01 (see ASCII preview below
// to confirm which of 01/02 is the edge-darkening shadow mask).
const SOURCES = {
  color: "PetalTextureColor.psd",
  lightmap: "PetalTextureLightmap.psd",
  noise: "PetalTextureNoise.psd",
  shadow01: "PetalTexture01.psd",
  shadow02: "PetalTexture02.psd",
};

function readPsdComposite(buf) {
  const channels = buf.readUInt16BE(12);
  const height = buf.readUInt32BE(14);
  const width = buf.readUInt32BE(18);

  let off = 26;
  off += 4 + buf.readUInt32BE(off); // color mode data
  off += 4 + buf.readUInt32BE(off); // image resources
  off += 4 + buf.readUInt32BE(off); // layer & mask info

  const compression = buf.readUInt16BE(off);
  off += 2;

  const planes = [];
  const planeSize = width * height;

  if (compression === 0) {
    for (let c = 0; c < channels; c++) {
      planes.push(Uint8Array.from(buf.subarray(off, off + planeSize)));
      off += planeSize;
    }
  } else if (compression === 1) {
    const totalLines = channels * height;
    const lineLens = new Array(totalLines);
    for (let i = 0; i < totalLines; i++) {
      lineLens[i] = buf.readUInt16BE(off);
      off += 2;
    }
    let li = 0;
    for (let c = 0; c < channels; c++) {
      const plane = new Uint8Array(planeSize);
      let p = 0;
      for (let y = 0; y < height; y++) {
        const end = off + lineLens[li++];
        while (off < end) {
          const n = buf.readInt8(off++);
          if (n >= 0) {
            for (let k = 0; k <= n; k++) plane[p++] = buf[off++];
          } else if (n !== -128) {
            const cnt = 1 - n;
            const val = buf[off++];
            for (let k = 0; k < cnt; k++) plane[p++] = val;
          }
        }
        off = end;
      }
      planes.push(plane);
    }
  } else {
    throw new Error("Unsupported PSD compression: " + compression);
  }

  return { width, height, channels, planes };
}

/** Returns alpha (0..1) per pixel in BOTTOM-up row order. */
function extractAlpha(psd) {
  const { width, height, channels, planes } = psd;
  const out = new Array(width * height);
  let i = 0;
  for (let y = height - 1; y >= 0; y--) {
    for (let x = 0; x < width; x++) {
      const idx = y * width + x;
      let a;
      if (channels >= 4) {
        a = planes[3][idx];
      } else {
        a =
          0.299 * planes[0][idx] +
          0.587 * planes[1][idx] +
          0.114 * planes[2][idx];
      }
      out[i++] = a / 255;
    }
  }
  return out;
}

function ascii(values, width, height) {
  const ramp = " .:-=+*#%@";
  let s = "";
  // ascii preview top-down for human reading (flip back)
  for (let y = height - 1; y >= 0; y--) {
    let line = "";
    for (let x = 0; x < width; x++) {
      const v = values[y * width + x];
      line += ramp[Math.min(ramp.length - 1, Math.floor(v * ramp.length))];
    }
    s += line + "\n";
  }
  return s;
}

const channels = {};
let W = 0,
  H = 0;
for (const [key, file] of Object.entries(SOURCES)) {
  const psd = readPsdComposite(readFileSync(DIR + file));
  W = psd.width;
  H = psd.height;
  const alpha = extractAlpha(psd);
  channels[key] = alpha;
  const min = Math.min(...alpha).toFixed(3);
  const max = Math.max(...alpha).toFixed(3);
  console.log(`\n=== ${key} (${file}) ${psd.channels}ch  min=${min} max=${max}`);
  console.log(ascii(alpha, psd.width, psd.height));
}

const fmt = (arr) => arr.map((v) => Number(v.toFixed(5))).join(",");
const banner =
  "// AUTO-GENERATED by scripts/extract-petal-channels.mjs — do not edit.\n" +
  "// Petal mask alpha arrays (0..1), bottom-up row order, size below.\n\n";
const body =
  banner +
  `export const PETAL_CHANNEL_WIDTH = ${W};\n` +
  `export const PETAL_CHANNEL_HEIGHT = ${H};\n\n` +
  `export const PETAL_CHANNELS = {\n` +
  `  color: [${fmt(channels.color)}],\n` +
  `  lightmap: [${fmt(channels.lightmap)}],\n` +
  `  noise: [${fmt(channels.noise)}],\n` +
  `  shadow01: [${fmt(channels.shadow01)}],\n` +
  `  shadow02: [${fmt(channels.shadow02)}],\n` +
  `} as const;\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, body);
console.log(`\nWrote ${OUT}`);
